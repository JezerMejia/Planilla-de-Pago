    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String id_str = this.idTF.getText();
        if (id_str.isBlank()) return;

        try {
            this.idEmpleado = Integer.parseInt(id_str);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingresa un n√∫mero", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Planilla.PlanillaPago planilla = this.administrador.getPlanillaPago();
        this.empleado = planilla.getTablaEmpleados().buscarEmpleado(this.idEmpleado);
        if (this.empleado == null) {
            this.empleadoTF.setText("");
            return;
        }
        this.empleadoTF.setText(this.empleado.getNombre());
    }//GEN-LAST:event_buscarActionPerformed

